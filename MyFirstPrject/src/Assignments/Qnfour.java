package Assignments;

public class Qnfour {

	public static void main(String[] args) {
		int a=10,b=20;
		System.out.println("Before Swapping");
		System.out.println("Enter a number" +a);
		System.out.println("Enter a number" +b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("After Swapping");
		System.out.println("Enter a number" +a);
		System.out.println("Enter a number" +b);	
	}

}
